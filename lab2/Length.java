package lab2.Length ;

public class Length {
    public static void main(String[] args) {
        System.out.println("Length of byte: " + Byte.SIZE + " bits");
        System.out.println("Length of short: " + Short.SIZE + " bits");
        System.out.println("Length of int: " + Integer.SIZE + " bits");
        System.out.println("Length of long: " + Long.SIZE + " bits");
        System.out.println("Length of float: " + Float.SIZE + " bits");
        System.out.println("Length of double: " + Double.SIZE + " bits");
        System.out.println("Length of char: " + Character.SIZE + " bits");
    }
}

